package br.gov.sp.fatec.pi.imobiliaria.model;

/**
 * A enumeração TipoImovel representa os diferentes tipos de imóvel.
 */
public enum TipoImovel {

    /**
     * Representa uma casa.
     */
    CASA,

    /**
     * Representa um apartamento.
     */
    APARTAMENTO,

    /**
     * Representa um terreno.
     */
    TERRENO,

    /**
     * Representa uma sala comercial.
     */
    SALA_COMERCIAL,

    /**
     * Representa um galpão.
     */
    GALPAO,

    /**
     * Representa uma chácara.
     */
    CHACARA,

    /**
     * Representa um sítio.
     */
    SITIO,

    /**
     * Representa uma kitnet.
     */
    KITNET,

    /**
     * Representa um loft.
     */
    LOFT,

    /**
     * Representa uma cobertura.
     */
    COBERTURA,

    /**
     * Representa um flat.
     */
    FLAT;

}
